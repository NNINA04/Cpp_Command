#pragma once

class GarageDoor
{
public:
    void Up()
    {
        cout << "Garage door goes up" << endl;
    }

    void Down()
    {
        cout << "Garage door goes down" << endl;
    }

    void LightOn()
    {
        cout << "Garage light turns ON" << endl;
    }

    void LightOff()
    {
        cout << "Garage light turns OFF" << endl;
    }
};

